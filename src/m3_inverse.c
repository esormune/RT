#include "rt.h"

/*
** Calculates the inverse of a 3x3 matrix.
*/
t_m3	m3_inverse(t_m3 m3)
{
	t_m3	new;
	double	det;
	double	inv_det;

	m3_set_identity(&new);
	det = m3.m[0][0] * (m3.m[1][1] * m3.m[2][2] - m3.m[2][1] * m3.m[1][2]) - \
		m3.m[0][1] * (m3.m[1][0] * m3.m[2][2] - m3.m[1][2] * m3.m[2][0]) + \
		m3.m[0][2] * (m3.m[1][0] * m3.m[2][1] - m3.m[1][1] * m3.m[2][0]);
	if (!det)
		return (new);
	inv_det = 1 / det;
	new.m[0][0] = (m3.m[1][1] * m3.m[2][2] - m3.m[2][1] * m3.m[1][2]) * inv_det;
	new.m[0][1] = (m3.m[0][2] * m3.m[2][1] - m3.m[0][1] * m3.m[2][2]) * inv_det;
	new.m[0][2] = (m3.m[0][1] * m3.m[1][2] - m3.m[0][2] * m3.m[1][1]) * inv_det;
	new.m[1][0] = (m3.m[1][2] * m3.m[2][0] - m3.m[1][0] * m3.m[2][2]) * inv_det;
	new.m[1][1] = (m3.m[0][0] * m3.m[2][2] - m3.m[0][2] * m3.m[2][0]) * inv_det;
	new.m[1][2] = (m3.m[1][0] * m3.m[0][2] - m3.m[0][0] * m3.m[1][2]) * inv_det;
	new.m[2][0] = (m3.m[1][0] * m3.m[2][1] - m3.m[2][0] * m3.m[1][1]) * inv_det;
	new.m[2][1] = (m3.m[2][0] * m3.m[0][1] - m3.m[0][0] * m3.m[2][1]) * inv_det;
	new.m[2][2] = (m3.m[0][0] * m3.m[1][1] - m3.m[1][0] * m3.m[0][1]) * inv_det;
	return (new);
}
